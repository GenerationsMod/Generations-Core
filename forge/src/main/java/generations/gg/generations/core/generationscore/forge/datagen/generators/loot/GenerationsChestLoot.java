	private void registerBallLootPoolUniform(BiConsumer<ResourceLocation, LootTable.Builder> output, ResourceLocation ball, float min, float max, Object... items) {
		registerBallLootPool(output, ball, UniformGenerator.between(min, max), items);
	}

	private void registerBallLootPoolConstant(BiConsumer<ResourceLocation, LootTable.Builder> output, ResourceLocation ball, float constant, Object... items) {
		registerBallLootPool(output, ball, ConstantValue.exactly(constant), items);
	}


	private void registerBallLootPool(BiConsumer<ResourceLocation, LootTable.Builder> output, ResourceLocation ball, NumberProvider provider, Object... items) {
		output.accept(ball, LootTable.lootTable().withPool(createPool(provider, process(items))));
	}

	public static Item[] process(Object[] objects) {
		return Arrays.stream(objects).map(GenerationsChestLoot::process).filter(Objects::nonNull).toArray(Item[]::new);
	}

	public static @Nullable Item process(Object object) {
		if(object instanceof RegistrySupplier<?> supplier) {
			return supplier.get() instanceof ItemLike item ? item.asItem() : null;
		} else return object instanceof ItemLike item ? item.asItem() : null;
	}

	private LootPool.Builder createPool(NumberProvider rolls, Item... items) {
		LootPool.Builder pool = LootPool.lootPool().setRolls(rolls);
		for (Item item : items)
			pool.add(LootItem.lootTableItem(item));

		return pool;
	}

	public static LootPoolSingletonContainer.Builder<?> key(String key) {
		return ResouceKeyEntry.resourceKeyContents(ResourceKey.create(Registries.ITEM, new ResourceLocation("generations_music", key)));
	}
